export const searchCurrencies = (query) => {
  if (!query || query.trim() === '') {
    return currencies;
  }
 
  const lowerQuery = query.toLowerCase().trim();
  return currencies.filter(currency =>
    currency.code.toLowerCase().includes(lowerQuery) ||
    currency.name.toLowerCase().includes(lowerQuery) ||
    currency.country.toLowerCase().includes(lowerQuery)
  );
};
 
export const getCurrencyById = (id) => {
  return currencies.find(currency => currency.id === parseInt(id));
};
 
export const getCurrencyByCode = (code) => {
  return currencies.find(currency => currency.code.toLowerCase() === code.toLowerCase());
};
 
export const getPopularCurrencies = () => {
  return currencies.slice(0, 10);
};
 
export const formatCurrencyValue = (value, currencyCode) => {
  const currency = getCurrencyByCode(currencyCode);
  if (!currency) return `${value} ${currencyCode}`;
 
  const formattedValue = typeof value === 'number' ? value.toFixed(2) : parseFloat(value).toFixed(2);
  return `${currency.symbol} ${formattedValue}`;
};